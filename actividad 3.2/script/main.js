let seleccion = Number(prompt("Selecciona a un personaje 1: 'Homero', 2: 'Marge', 3: 'Lisa', 4: 'Bart', 5: 'Maggie'"))


switch(seleccion){
    case 1:
        alert("Elegiste a Homero")
        break
    case 2:
        alert("Elegiste a Marge")
        break
    case 3:
        alert("Elegiste a Lisa")
        break
    case 4:
        alert("Elegiste a Bart")
        break
    case 5:
        alert("Elegiste a Maggie")
        break
    default:
        alert("No elegiste correctamente")
        break
}